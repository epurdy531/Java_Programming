public class Book
{
   private String title;
   private int pages;
   
   public String getBookTitle()
   {
      return title;
   } 
   public int getBookPages()
   {
      return pages;
   }
   public void setBookTitle(String bookTitle)
   {
      title = bookTitle;
   }
   public void setBookPages(int bookPages)
   {
      pages = bookPages;
   }
}